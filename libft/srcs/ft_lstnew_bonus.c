#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*nodo;

	nodo = (t_list *)malloc(sizeof(t_list));
	if (nodo)
	{
		nodo -> content = content;
		nodo -> next = NULL;
		return (nodo);
	}
	return (NULL);
}
